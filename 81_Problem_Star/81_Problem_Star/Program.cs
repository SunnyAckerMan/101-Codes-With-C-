Console.WriteLine();

for (int row = 8; row >= 1; --row)//row = 7654321
{
    for (int col = 1; col <= row; ++col) //column = 23456789 (exit in 9)
    {
        Console.Write("*");
    }

    Console.WriteLine();
}

